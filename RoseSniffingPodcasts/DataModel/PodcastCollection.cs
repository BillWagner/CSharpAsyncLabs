        private ObservableCollection<PodcastSeries> _allGroups = new ObservableCollection<PodcastSeries>();
        public ObservableCollection<PodcastSeries> AllGroups
        {
            get 
            {
                if (downloadTask != null)
                {
                    if (downloadTask.IsFaulted)
                    {
                        _allGroups.Clear();
                        downloadTask = null;
                    }
                    else if (downloadTask.IsCompleted)
                        downloadTask = null;
                }
                return this._allGroups; 
            }
        }

        public static IEnumerable<PodcastSeries> GetGroups(string uniqueId)
        {
            if (!uniqueId.Equals("AllGroups")) throw new ArgumentException("Only 'AllGroups' is supported as a collection of groups");
            
            return podcastCollection.AllGroups;
        }

        public static PodcastSeries GetGroup(string uniqueId)
        {
            // Simple linear search is acceptable for small data sets
            var matches = podcastCollection.AllGroups.Where((group) => group.UniqueId.Equals(uniqueId));
            if (matches.Count() == 1) return matches.First();
            return null;
        }

        public static PodcastEpisode GetItem(string uniqueId)
        {
            // Simple linear search is acceptable for small data sets
            var matches = podcastCollection.AllGroups.SelectMany(group => group.Items).Where((item) => item.UniqueId.Equals(uniqueId));
            if (matches.Count() == 1) return matches.First();
            return null;
        }

        public PodcastCollection() :
            this(new FeedDownloader())
        {
        }

        public PodcastCollection(IFeedDownloader downloader)
        {
            this.downloader = downloader;
            if (Windows.ApplicationModel.DesignMode.DesignModeEnabled)
                this.downloadTask = LoadDesignMode();
            else
                this.downloadTask = RetrievePodcasts();
        }
        
        private async Task downloadPodcast(string addr)
        {
            var feed = await downloader.RetrieveFeed(addr);
            var series = new PodcastSeries(feed.Id, feed.Title, feed.ImagePath, feed.Description);
            AllGroups.Add(series);
            
            foreach (var item in feed.Episodes)
                series.Items.Add(new PodcastEpisode(item.ItemID, item.ItemTitle, item.Subtitle, feed.ImagePath, 
                    item.DownloadURL, item.Description, series));
        }

        private async Task RetrievePodcasts()
        {
            foreach (var addr in subscriptions)
            {
                await downloadPodcast(addr);
            }
        }

